-module(indira).

-export([start_link/0]).
-export([start/0]).

%-----------------------------------------------------------------------------

-record(state, {
  % nuffin
}).

%-----------------------------------------------------------------------------
% public API
%-----------------------------------------------------------------------------

% nuffin yet

%-----------------------------------------------------------------------------
% public API for supervision tree
%-----------------------------------------------------------------------------

start_link() ->
  State = init(),
  Pid = spawn_link(fun() -> loop(State) end),
  {ok, Pid}.

start() ->
  application:start(indira).

%-----------------------------------------------------------------------------
% internals
%-----------------------------------------------------------------------------

init() ->
  #state{}.

loop(State = #state{}) ->
  io:fwrite("this is indira (~p)~n", [self()]),
  timer:sleep(1000),
  loop(State).

